def main():
    global MODEL, CONFIG, DEVICE, STEP

    p = argparse.ArgumentParser(description="HLM-v9 Multiscale Chat Interface")
    p.add_argument("--checkpoint", required=True, help="Path to .pt checkpoint")
    p.add_argument("--device", default=None, help="Device (default: cuda if available)")
    p.add_argument("--port", type=int, default=7860)
    p.add_argument("--host", default="127.0.0.1")
    args = p.parse_args()

    if args.device is None:
        DEVICE = "cuda" if torch.cuda.is_available() else "cpu"
    else:
        DEVICE = args.device

    MODEL, CONFIG, STEP = load_model(args.checkpoint, DEVICE)

    log.info(f"Starting chat server on http://{args.host}:{args.port}")
    import webbrowser
    webbrowser.open(f"http://{args.host}:{args.port}")
    app.run(host=args.host, port=args.port, debug=False, threaded=True)


