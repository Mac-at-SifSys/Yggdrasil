# ── Model loading ────────────────────────────────────────────────────────

def load_model(checkpoint_path: str, device: str):
    """Load HLM-v9 from checkpoint."""
    log.info(f"Loading checkpoint: {checkpoint_path}")
    ckpt = torch.load(checkpoint_path, map_location=device, weights_only=False)

    config = ckpt["config"]
    log.info(f"  Config: {config}")

    mod = get_hlm_v9_module()
    HLMv9 = mod.HLMv9

    model = HLMv9(
        grid_size=config.get("G", 4),
        n_tracks=config.get("T", 8),
        n_passes=config.get("P", 2),
        n_blocks=config.get("blocks", 24),
        mlp_hidden=config.get("H", 4096),
    )
    model.use_checkpoint = False  # No gradient checkpointing at inference

    state_dict = ckpt["model"]
    # Strip _orig_mod. prefix (from torch.compile)
    cleaned = {k.replace("_orig_mod.", ""): v for k, v in state_dict.items()}
    missing, unexpected = model.load_state_dict(cleaned, strict=False)
    if missing:
        log.warning(f"  Missing keys ({len(missing)}): {missing[:5]}...")
    if unexpected:
        log.warning(f"  Unexpected keys ({len(unexpected)}): {unexpected[:5]}...")

    model.to(device)
    model.eval()

    step = ckpt.get("step", "unknown")
    total_bytes = ckpt.get("total_bytes", 0)
    loss = ckpt.get("loss", 0)
    n_params = sum(p.numel() for p in model.parameters())
    log.info(f"  Loaded step {step}, {n_params:,} params, loss={loss:.4f}")
    log.info(f"  Total training bytes: {total_bytes:,}")
    return model, config, step


# ── Byte-level generation ────────────────────────────────────────────────

@torch.no_grad()
def generate_stream(
    prompt: str,
    max_new_bytes: int = 512,
    temperature: float = 0.8,
    top_k: int = 40,
    top_p: float = 0.9,
    repetition_penalty: float = 1.1,
):
    """Yield decoded text chunks as they're generated byte-by-byte."""
    # Encode prompt to raw UTF-8 bytes
    prompt_bytes = list(prompt.encode("utf-8"))
    byte_seq = prompt_bytes[:]
    max_seq = MODEL.max_seq_len  # 512

    generated_bytes = []
    pending_bytes = bytearray()  # Buffer for incomplete UTF-8 sequences

    for _ in range(max_new_bytes):
        # Crop to context window
        ctx = byte_seq[-max_seq:]
        input_tensor = torch.tensor([ctx], dtype=torch.long, device=DEVICE)

        outputs = MODEL(input_tensor)
        logits = outputs["logits"][:, -1, :]  # (1, 256)

        # Repetition penalty on recent bytes
        if repetition_penalty != 1.0 and generated_bytes:
            for prev_byte in set(generated_bytes[-64:]):
                if logits[0, prev_byte] > 0:
                    logits[0, prev_byte] /= repetition_penalty
                else:
                    logits[0, prev_byte] *= repetition_penalty

        if temperature == 0:
            next_byte = logits.argmax(dim=-1).item()
        else:
            logits = logits / temperature

            if top_k > 0:
                v, _ = torch.topk(logits, min(top_k, 256))
                logits[logits < v[:, [-1]]] = float("-inf")

            if top_p < 1.0:
                sorted_logits, sorted_indices = torch.sort(logits, descending=True)
                cum_probs = torch.cumsum(F_func.softmax(sorted_logits, dim=-1), dim=-1)
                remove = cum_probs > top_p
                remove[:, 1:] = remove[:, :-1].clone()
                remove[:, 0] = False
                indices_to_remove = remove.scatter(1, sorted_indices, remove)
                logits[indices_to_remove] = float("-inf")

            probs = F_func.softmax(logits, dim=-1)
            next_byte = torch.multinomial(probs, num_samples=1).item()

        generated_bytes.append(next_byte)
        byte_seq.append(next_byte)

        # Try to decode pending bytes + new byte as UTF-8
        pending_bytes.append(next_byte)
        try:
            text_chunk = pending_bytes.decode("utf-8")
            pending_bytes.clear()

            # Get blade profile periodically (every ~64 bytes to avoid slowdown)
            blades = None
            if len(generated_bytes) % 64 == 0 or len(generated_bytes) == 1:
                ctx_tensor = torch.tensor([byte_seq[-max_seq:]], dtype=torch.long, device=DEVICE)
                blades = get_blade_profile(ctx_tensor)

            yield text_chunk, blades
        except UnicodeDecodeError:
            # Incomplete multi-byte sequence, keep buffering
            if len(pending_bytes) > 4:
                # Corrupted — flush as replacement char
                pending_bytes.clear()
                yield "\ufffd", None

    # Flush any remaining bytes
    if pending_bytes:
        try:
            yield pending_bytes.decode("utf-8", errors="replace"), None
        except Exception:
            pass


@torch.no_grad()
def get_blade_profile(byte_seq_tensor):
    """Extract blade activation profile by hooking into the model's final hidden state.

    Runs a forward pass and applies the blade_classifier to the last position.
    """
    try:
        # We need the hidden state before output_head.
        # Use a forward hook on final_norm to capture it.
        hidden = {}

        def hook_fn(module, input, output):
            hidden["h"] = output

        handle = MODEL.final_norm.register_forward_hook(hook_fn)
        MODEL(byte_seq_tensor)
        handle.remove()

        if "h" in hidden:
            h = hidden["h"][:, -1, :]  # (1, d_model)
            blade_logits = MODEL.blade_classifier(h)  # (1, 8)
            blade_probs = F_func.softmax(blade_logits, dim=-1)
            return blade_probs[0].cpu().tolist()
        return None
    except Exception as e:
        log.debug(f"Blade profile error: {e}")
        return None


